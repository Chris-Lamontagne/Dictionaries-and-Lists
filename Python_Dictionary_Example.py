# Create a dictionary
student = {
    "name": "Alice",
    "age": 25,
    "grade": "A",
    "major": "Computer Science"
}

# Access values using keys
print(student["name"])

# Modify values
student["grade"] = "A+"
print(student)

# Add a new key-value pair
student["graduation_year"] = 2025
print(student)
